package part1;

public class Car {
    private int position;
    private int carLength;

    public Car(int position, int carLength){

    }

    public void setPosition(int value)  {
        position = value;
    }

    public int getPosition()    {
        return position;
    }

    public void setCarLength(int value) {
        carLength = value;
    }

    public int getCarLength()   {
        return carLength;
    }


}
